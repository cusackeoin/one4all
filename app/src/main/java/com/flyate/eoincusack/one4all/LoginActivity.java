    public boolean validate() {
        boolean valid = true;

        String email = _emailText.getText().toString();
        String password = _passwordText.getText().toString();

        if (email.isEmpty() || !android.util.Patterns.EMAIL_ADDRESS.matcher(email).matches()) {
            _emailText.setError("enter a valid email address");
            valid = false;
        } else {
            _emailText.setError(null);
        }

        if (password.isEmpty() || password.length() < 4 || password.length() > 10) {
            _passwordText.setError("between 4 and 10 alphanumeric characters");
            valid = false;
        } else {
            _passwordText.setError(null);
        }

        return valid;
    }

    public String searchLoginDetails(String pass, String email){
        all4oneDBHelper helper = new all4oneDBHelper(this);
        SQLiteDatabase db = helper.getReadableDatabase();

        String query = "select email, password from " + all4oneDB.FeedEntry.TABLE_NAME + " where email='" + email + "' and password='" + pass + "'";
        Cursor c = db.rawQuery(query, null);
        String a, b;
        b = "not found";
        if(c.moveToFirst()) {
            do {
                a = c.getString(0);

                if (a.equals(email)) {
                    b = c.getString(1);
                    break;
                }
            } while(c.moveToNext());
        }

        return b;

    }

